# Se declara la variable "miVariable" y se le asigna el valor 10.
miVariable = 10

# Incrementar "miVariable" en 1 y reasignar el valor.
print(miVariable)
miVariable = miVariable + 1
print(miVariable)

# Incrementar "miVariable" en 1 usando el operador de +=.
miVariable += 1
print(miVariable)

# Decrementar "miVariable" en 2 usando el operador de -=.
miVariable -= 2
print(miVariable)

# Multiplicar "miVariable" por 3 usando el operador de *=.
miVariable *= 3
print(miVariable)

# Dividir "miVariable" por 2 usando el operador de /=.
miVariable /= 2
print(miVariable)
