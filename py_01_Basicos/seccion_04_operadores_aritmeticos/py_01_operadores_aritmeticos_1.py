# Asignación de valores a las variables "operadorA" y "operadorB"
operadorA = 3
operadorB = 2

# Suma de las variables "operadorA" y "operadorB" y almacenamiento en la variable "suma"
suma = operadorA + operadorB

# Impresión de un mensaje con los valores de las variables
print(f"El primer número de A: {operadorA} y el segundo número de B: {operadorB}")

# Impresión del resultado de la suma
print(f"La suma: {suma}")
