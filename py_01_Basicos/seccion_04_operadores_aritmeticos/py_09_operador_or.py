# Se define la variable "vacaciones" como False (no está de vacaciones).
vacaciones = False

# Se define la variable "diadescanso" como True (está de día de descanso).
diadescanso = True

# Se comprueba si está de vacaciones o de día de descanso.
if vacaciones or diadescanso:
    # Si está de vacaciones o de día de descanso, puede asistir al juego.
    print("Puede asistir al juego")

else:
    # Si no está de vacaciones o de día de descanso, tiene deberes por hacer.
    print("Tiene deberes por hacer")
