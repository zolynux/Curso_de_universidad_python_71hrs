# Declaración de variables
miGrupoFavorito = "Metallica"  # Cadena (String)
comentario = "The Best Rock Band"

# Impresión de la variable "miGrupoFavorito" con concatenación
# print("Mi grupo favorito es: " + miGrupoFavorito + " " + comentario)
print("Mi grupo favorito es:", miGrupoFavorito, comentario)

# Concatenación de variables de tipo cadena
numero1 = "1"  # No es tipo entero
numero2 = "2"
print(numero1 + numero2)  # Salida: "12"
print("concatenación:", numero1 + numero2)

# Suma de variables de tipo entero
numero1 = 1  # Es tipo entero
numero2 = 2
print(numero1 + numero2)  # Salida: 3
print("concatenación:", numero1 + numero2)

# Conversión de tipo de dato y suma
print("Tipo de datos convertido cadena a entero:")
print("Suma:", int(numero1) + int(numero2))  # Salida: 3
