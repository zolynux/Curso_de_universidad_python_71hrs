# Asignación de un valor booleano True a la variable "miVariable"
miVariable = True

# Impresión del valor de "miVariable"
print(miVariable)

# Asignación de un valor booleano False a la variable "miVariable"
miVariable = False

# Impresión del valor de "miVariable"
print(miVariable)

# Asignación del resultado de la comparación 3 > 2 a la variable "miVariable" (True)
miVariable = 3 > 2

# Impresión del valor de "miVariable"
print(miVariable)

# Asignación del resultado de la comparación 1 > 2 a la variable "miVariable" (False)
miVariable = 1 > 2

# Impresión del valor de "miVariable"
print(miVariable)

# Condición: si "miVariable" es True, se imprime "El resultado fue verdadero"
# Si "miVariable" es False, se imprime "El resultado fue falso"
if miVariable:
    print("El resultado fue verdadero")
else:
    print("El resultado fue falso")

