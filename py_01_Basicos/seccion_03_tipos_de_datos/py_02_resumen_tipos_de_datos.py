# Entero (int)
x = 10  # Asigna el valor 10 (tipo entero)
print(x)  # Imprime el valor
print(type(x))  # Imprime el tipo de dato (int)

# Decimal (float)
x = 14.5  # Asigna el valor 14.5 (tipo decimal)
print(x)  # Imprime el valor
print(type(x))  # Imprime el tipo de dato (float)

# Cadena (str)
x = "Hola mundo"  # Asigna el texto "Hola mundo" (tipo cadena)
print(x)  # Imprime el texto
print(type(x))  # Imprime el tipo de dato (str)

# Booleano (bool)
x = True  # Asigna el valor Verdadero (tipo booleano)
print(x)  # Imprime True
print(type(x))  # Imprime el tipo de dato (bool)

x = False  # Asigna el valor Falso (tipo booleano)
print(x)  # Imprime False
print(type(x))  # Imprime el tipo de dato (bool)
