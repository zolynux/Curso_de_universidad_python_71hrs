# Asignación de un número entero a la variable "x"
x = 10  # Numero entero

# Impresión del valor de "x"
print(x)

# Impresión del tipo de dato de "x"
print(type(x))  # <class 'int'>

# Asignación de una cadena de caracteres a la variable "x"
x = "Hola mundo"  # Tipo cadena

# Asignación de una cadena de caracteres a la variable "x" (usando comillas simples)
x = "Hola mundo"  # Tipo cadena

# Asignación de una cadena de caracteres a la variable "x" con anotación de tipo
x: str = "Hola mundo"  # Tipo cadena

# Impresión del valor de "x"
print(x)

# Impresión del tipo de dato de "x"
print(type(x))  # <class 'str'>

# Asignación de un número decimal a la variable "x"
x = 10.5  # tipo flotante

# Impresión del valor de "x"
print(x)

# Impresión del tipo de dato de "x"
print(type(x))  # <class float>

# Asignación de un valor booleano a la variable "x"
x = True  # tipo booleano

# Impresión del valor de "x"
print(x)

# Impresión del tipo de dato de "x"
print(type(x))  # <class 'bool'>
