# Se define una variable "condicion" con valor True
condicion = True

# Si la condición es True, se ejecuta el bloque "Condición Verdadera"
if condicion:
    print("Condición Verdadera")

# Si la condición es False, se ejecuta el bloque "Condición falsa"
else:
    print("Condición falsa")

# Operador ternario:
# Si la condición es True, se imprime "Condición verdadera", sino se imprime "Condición falsa"
print("Condición verdadera") if condicion else print("Condición falsa")
