# sección 32: Profundizando más en la clase str en Python

# Multiplicación str
resultado = 3 * "Hola"
print(f"Resultado: {resultado}")

# Multiplicación tuplas
resultado = 5 * ("Hola", "Mundo")
print(f"Resultado: {resultado}")

# Multiplicación listas
resultado = 10 * [0]
print(f"Resultado: {resultado},\nlargo: {len(resultado)}")
