# Conversión base numérica
# Convertir un tipo entero, incluyendo base
# Base de Decimal
a = int("23", 10)
print(f"a: {a}")
# Base de Binario
a = int("10111", 2)
print(f"a: {a}")
# Base Octal
a = int("27", 8)
print(f"a: {a}")
# Base Hexadecimal
a = int("17", 16)
print(f"a: {a}")
# base 5
a = int("34", 5)
print(f"a: {a}")
a = int("344", 5)
print(f"a: {a}")
